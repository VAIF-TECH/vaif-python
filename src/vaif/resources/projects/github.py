        self._github = github

        self.branch = async_to_streamed_response_wrapper(
            github.branch,
        )
        self.connect = async_to_streamed_response_wrapper(
            github.connect,
        )
        self.disconnect = async_to_streamed_response_wrapper(
            github.disconnect,
        )
        self.get_branches = async_to_streamed_response_wrapper(
            github.get_branches,
        )
        self.get_repos = async_to_streamed_response_wrapper(
            github.get_repos,
        )
        self.get_status = async_to_streamed_response_wrapper(
            github.get_status,
        )
        self.get_tracked_repos = async_to_streamed_response_wrapper(
            github.get_tracked_repos,
        )
        self.pull = async_to_streamed_response_wrapper(
            github.pull,
        )
        self.push = async_to_streamed_response_wrapper(
            github.push,
        )
